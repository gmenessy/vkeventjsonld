CREATE TABLE VK_ROLE (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100) NOT NULL UNIQUE
);

CREATE TABLE VK_USER_ROLE (
    USER_ID NUMBER(19) NOT NULL,
    ROLE_ID NUMBER(19) NOT NULL,
    CONSTRAINT PK_USER_ROLE PRIMARY KEY (USER_ID, ROLE_ID),
    CONSTRAINT FK_UR_USER FOREIGN KEY (USER_ID) REFERENCES VK_USER(ID),
    CONSTRAINT FK_UR_ROLE FOREIGN KEY (ROLE_ID) REFERENCES VK_ROLE(ID)
);

-- Reserviert: Verknuepfung Nutzer<->Veranstalter (bewusst offen; Selbsteintrag nutzt Freitext).
CREATE TABLE VK_USER_PARTY (
    USER_ID NUMBER(19) NOT NULL,
    PARTY_ID NUMBER(19) NOT NULL,
    RELATION_TYPE VARCHAR2(50) DEFAULT 'EDITOR',
    VERIFIED CHAR(1) DEFAULT 'N' CHECK (VERIFIED IN ('Y','N')),
    CONSTRAINT PK_USER_PARTY PRIMARY KEY (USER_ID, PARTY_ID),
    CONSTRAINT FK_UP_USER FOREIGN KEY (USER_ID) REFERENCES VK_USER(ID),
    CONSTRAINT FK_UP_PARTY FOREIGN KEY (PARTY_ID) REFERENCES VK_PARTY(ID)
);

CREATE TABLE VK_EVENT_APPROVAL (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EVENT_ID NUMBER(19) NOT NULL,
    STATUS VARCHAR2(50) NOT NULL,
    SUBMITTED_BY NUMBER(19),
    SUBMITTED_AT TIMESTAMP,
    REVIEWED_BY NUMBER(19),
    REVIEWED_AT TIMESTAMP,
    REVIEW_NOTE CLOB,
    CONSTRAINT FK_APPROVAL_EVENT FOREIGN KEY (EVENT_ID) REFERENCES VK_EVENT(ID)
);

CREATE TABLE VK_EVENT_VERSION (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EVENT_ID NUMBER(19) NOT NULL,
    VERSION_NO NUMBER(10) NOT NULL,
    SNAPSHOT_JSON CLOB NOT NULL,
    CREATED_BY NUMBER(19),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_EVENT_VERSION_EVENT FOREIGN KEY (EVENT_ID) REFERENCES VK_EVENT(ID),
    CONSTRAINT UQ_EVENT_VERSION UNIQUE (EVENT_ID, VERSION_NO)
);

CREATE TABLE VK_IMPORT_JOB (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PUBLIC_ID VARCHAR2(36) NOT NULL UNIQUE,
    MANDANT_ID NUMBER(19) NOT NULL,
    VK_ID NUMBER(19) NOT NULL,
    IMPORT_TYPE VARCHAR2(50) NOT NULL,
    STATUS VARCHAR2(50) NOT NULL,
    FILE_NAME VARCHAR2(500),
    CREATED_BY NUMBER(19),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    FINISHED_AT TIMESTAMP,
    ERROR_MESSAGE CLOB
);

CREATE TABLE VK_IMPORT_ITEM (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IMPORT_JOB_ID NUMBER(19) NOT NULL,
    ROW_NO NUMBER(10),
    STATUS VARCHAR2(50) NOT NULL,
    EXTERNAL_ID VARCHAR2(500),
    EVENT_ID NUMBER(19),
    RAW_JSON CLOB,
    ERROR_MESSAGE CLOB,
    CONSTRAINT FK_IMPORT_ITEM_JOB FOREIGN KEY (IMPORT_JOB_ID) REFERENCES VK_IMPORT_JOB(ID)
);

-- Reserviert: asynchrone Export-Jobs (Export laeuft derzeit synchron/streamend).
CREATE TABLE VK_EXPORT_JOB (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PUBLIC_ID VARCHAR2(36) NOT NULL UNIQUE,
    EXPORT_TYPE VARCHAR2(50) NOT NULL,
    STATUS VARCHAR2(50) NOT NULL,
    FILTER_JSON CLOB,
    FILE_NAME VARCHAR2(500),
    CREATED_BY NUMBER(19),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    FINISHED_AT TIMESTAMP,
    ERROR_MESSAGE CLOB
);

CREATE TABLE VK_AUDIT_LOG (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MANDANT_ID NUMBER(19) NOT NULL,
    VK_ID NUMBER(19) NOT NULL,
    USER_ID NUMBER(19),
    ENTITY_TYPE VARCHAR2(100) NOT NULL,
    ENTITY_ID NUMBER(19),
    ACTION VARCHAR2(100) NOT NULL,
    OLD_VALUE_JSON CLOB,
    NEW_VALUE_JSON CLOB,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE INDEX IDX_EVENT_PLACE ON VK_EVENT(PLACE_ID);
