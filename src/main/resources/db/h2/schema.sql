CREATE TABLE VK_EVENT_PARTY_ROLE (
    EVENT_ID NUMBER(19) NOT NULL,
    PARTY_ID NUMBER(19) NOT NULL,
    ROLE_TYPE VARCHAR2(50) NOT NULL,
    SORT_ORDER NUMBER(5) DEFAULT 0,
    CONSTRAINT PK_EVENT_PARTY_ROLE PRIMARY KEY (EVENT_ID, PARTY_ID, ROLE_TYPE),
    CONSTRAINT FK_EPR_EVENT FOREIGN KEY (EVENT_ID) REFERENCES VK_EVENT(ID),
    CONSTRAINT FK_EPR_PARTY FOREIGN KEY (PARTY_ID) REFERENCES VK_PARTY(ID)
);

CREATE TABLE VK_CATEGORY (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PARENT_ID NUMBER(19),
    NAME VARCHAR2(200) NOT NULL,
    SLUG VARCHAR2(200) NOT NULL UNIQUE,
    DESCRIPTION VARCHAR2(1000),
    SCHEMA_TYPE VARCHAR2(80),
    ICON VARCHAR2(100),
    SORT_ORDER NUMBER(5) DEFAULT 0,
    ACTIVE CHAR(1) DEFAULT 'Y' CHECK (ACTIVE IN ('Y','N')),
    CONSTRAINT FK_CATEGORY_PARENT FOREIGN KEY (PARENT_ID) REFERENCES VK_CATEGORY(ID)
);

CREATE TABLE VK_EVENT_CATEGORY (
    EVENT_ID NUMBER(19) NOT NULL,
    CATEGORY_ID NUMBER(19) NOT NULL,
    PRIMARY_FLAG CHAR(1) DEFAULT 'N' CHECK (PRIMARY_FLAG IN ('Y','N')),
    CONSTRAINT PK_EVENT_CATEGORY PRIMARY KEY (EVENT_ID, CATEGORY_ID),
    CONSTRAINT FK_EC_EVENT FOREIGN KEY (EVENT_ID) REFERENCES VK_EVENT(ID),
    CONSTRAINT FK_EC_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES VK_CATEGORY(ID)
);

CREATE TABLE VK_KEYWORD (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(200) NOT NULL UNIQUE,
    SLUG VARCHAR2(200) NOT NULL UNIQUE
);

CREATE TABLE VK_EVENT_KEYWORD (
    EVENT_ID NUMBER(19) NOT NULL,
    KEYWORD_ID NUMBER(19) NOT NULL,
    CONSTRAINT PK_EVENT_KEYWORD PRIMARY KEY (EVENT_ID, KEYWORD_ID),
    CONSTRAINT FK_EK_EVENT FOREIGN KEY (EVENT_ID) REFERENCES VK_EVENT(ID),
    CONSTRAINT FK_EK_KEYWORD FOREIGN KEY (KEYWORD_ID) REFERENCES VK_KEYWORD(ID)
);

CREATE TABLE VK_OFFER (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EVENT_ID NUMBER(19) NOT NULL,
    NAME VARCHAR2(300),
    PRICE NUMBER(12,2),
    PRICE_CURRENCY VARCHAR2(3) DEFAULT 'EUR',
