-- Reserviert fuer spaeteren Sprint: erweiterte Personendaten (noch nicht im Code verdrahtet).
CREATE TABLE VK_PERSON_DETAILS (
    PARTY_ID NUMBER(19) PRIMARY KEY,
    GIVEN_NAME VARCHAR2(200),
    FAMILY_NAME VARCHAR2(200),
    JOB_TITLE VARCHAR2(300),
    CONSTRAINT FK_PERSON_PARTY FOREIGN KEY (PARTY_ID) REFERENCES VK_PARTY(ID)
