-- H2-Dev-Schema: strukturgleich zum Oracle-Schema, Typen H2-kompatibel
-- (TIMESTAMP WITH LOCAL TIME ZONE -> TIMESTAMP, SYSTIMESTAMP -> CURRENT_TIMESTAMP).

CREATE TABLE VK_ADDRESS (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    STREET_ADDRESS VARCHAR2(500),
    POSTAL_CODE VARCHAR2(30),
    LOCALITY VARCHAR2(200),
    REGION VARCHAR2(200),
    COUNTRY_CODE VARCHAR2(2) DEFAULT 'DE',
    ADDRESS_TEXT VARCHAR2(1000)
);

CREATE TABLE VK_PLACE (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PUBLIC_ID VARCHAR2(36) NOT NULL UNIQUE,
    MANDANT_ID NUMBER(19) NOT NULL,
    VK_ID NUMBER(19) NOT NULL,
    NAME VARCHAR2(300) NOT NULL,
    DESCRIPTION CLOB,
    ADDRESS_ID NUMBER(19),
    LATITUDE NUMBER(10,7),
    LONGITUDE NUMBER(10,7),
    URL VARCHAR2(1000),
    SAME_AS_URL VARCHAR2(1000),
    ACCESSIBILITY_NOTE VARCHAR2(2000),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_PLACE_ADDRESS FOREIGN KEY (ADDRESS_ID) REFERENCES VK_ADDRESS(ID)
);

CREATE TABLE VK_VIRTUAL_LOCATION (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(300),
    URL VARCHAR2(1000) NOT NULL,
    ACCESS_HINT VARCHAR2(2000),
    PLATFORM VARCHAR2(200)
);

CREATE TABLE VK_REGISTRY (
    MANDANT_ID NUMBER(19) NOT NULL,
    VK_ID NUMBER(19) NOT NULL,
    NAME VARCHAR2(300) NOT NULL,
    SLUG VARCHAR2(200),
    ACTIVE CHAR(1) DEFAULT 'Y' CHECK (ACTIVE IN ('Y','N')),
    CONSTRAINT PK_VK_REGISTRY PRIMARY KEY (MANDANT_ID, VK_ID)
);

-- Reserviert fuer spaeteren Sprint: Veranstaltungsserien/Wiederholungen (noch nicht im Code verdrahtet).
CREATE TABLE VK_EVENT_SERIES (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PUBLIC_ID VARCHAR2(36) NOT NULL UNIQUE,
    TITLE VARCHAR2(300) NOT NULL,
    DESCRIPTION CLOB,
    RECURRENCE_RULE VARCHAR2(1000),
    START_DATE DATE,
    END_DATE DATE,
    CREATED_BY NUMBER(19),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE VK_EVENT (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PUBLIC_ID VARCHAR2(36) NOT NULL UNIQUE,
    MANDANT_ID NUMBER(19) NOT NULL,
    VK_ID NUMBER(19) NOT NULL,
    SCHEMA_TYPE VARCHAR2(80) DEFAULT 'Event' NOT NULL,
    TITLE VARCHAR2(300) NOT NULL,
    SHORT_DESCRIPTION VARCHAR2(1000),
    DESCRIPTION CLOB,
    START_AT TIMESTAMP NOT NULL,
    END_AT TIMESTAMP,
    DOOR_TIME TIMESTAMP,
    DURATION_ISO VARCHAR2(50),
    ATTENDANCE_MODE VARCHAR2(30) DEFAULT 'OFFLINE' NOT NULL,
    EVENT_STATUS VARCHAR2(50) DEFAULT 'EventScheduled' NOT NULL,
    WORKFLOW_STATUS VARCHAR2(50) DEFAULT 'DRAFT' NOT NULL,
    IS_ACCESSIBLE_FOR_FREE CHAR(1) DEFAULT 'N' CHECK (IS_ACCESSIBLE_FOR_FREE IN ('Y','N')),
    MAX_ATTENDEE_CAPACITY NUMBER(10),
    REMAINING_ATTENDEE_CAPACITY NUMBER(10),
    PLACE_ID NUMBER(19),
    VIRTUAL_LOCATION_ID NUMBER(19),
    SUPER_EVENT_ID NUMBER(19),
    SERIES_ID NUMBER(19),
    CANONICAL_URL VARCHAR2(1000),
    SAME_AS_URL VARCHAR2(1000),
    LANGUAGE_CODE VARCHAR2(20) DEFAULT 'de',
    SEARCH_TEXT CLOB,
    CREATED_BY NUMBER(19),
    UPDATED_BY NUMBER(19),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP,
    PUBLISHED_AT TIMESTAMP,
    CONSTRAINT FK_EVENT_PLACE FOREIGN KEY (PLACE_ID) REFERENCES VK_PLACE(ID),
    CONSTRAINT FK_EVENT_VLOC FOREIGN KEY (VIRTUAL_LOCATION_ID) REFERENCES VK_VIRTUAL_LOCATION(ID),
    CONSTRAINT FK_EVENT_SUPER FOREIGN KEY (SUPER_EVENT_ID) REFERENCES VK_EVENT(ID),
    CONSTRAINT FK_EVENT_SERIES FOREIGN KEY (SERIES_ID) REFERENCES VK_EVENT_SERIES(ID)
);

CREATE TABLE VK_PARTY (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PUBLIC_ID VARCHAR2(36) NOT NULL UNIQUE,
    MANDANT_ID NUMBER(19) NOT NULL,
    VK_ID NUMBER(19) NOT NULL,
    PARTY_TYPE VARCHAR2(30) NOT NULL,
    DISPLAY_NAME VARCHAR2(300) NOT NULL,
    EMAIL VARCHAR2(320),
    TELEPHONE VARCHAR2(100),
    URL VARCHAR2(1000),
    SAME_AS_URL VARCHAR2(1000),
    ADDRESS_ID NUMBER(19),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_PARTY_ADDRESS FOREIGN KEY (ADDRESS_ID) REFERENCES VK_ADDRESS(ID)
);

-- Reserviert fuer spaeteren Sprint: erweiterte Organisationsdaten (noch nicht im Code verdrahtet).
CREATE TABLE VK_ORGANIZATION_DETAILS (
