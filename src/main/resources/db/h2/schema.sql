CREATE INDEX IDX_CATEGORY_PARENT ON VK_CATEGORY(PARENT_ID);
CREATE INDEX IDX_EVENT_CAT_CAT ON VK_EVENT_CATEGORY(CATEGORY_ID);
CREATE INDEX IDX_EVENT_ROLE_PARTY ON VK_EVENT_PARTY_ROLE(PARTY_ID, ROLE_TYPE);
CREATE INDEX IDX_EVENT_TENANT_PUB_START ON VK_EVENT(MANDANT_ID, VK_ID, WORKFLOW_STATUS, START_AT, ID);
