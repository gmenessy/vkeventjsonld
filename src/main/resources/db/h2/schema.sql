    AVAILABILITY VARCHAR2(100),
    VALID_FROM TIMESTAMP,
    VALID_THROUGH TIMESTAMP,
    CONSTRAINT FK_OFFER_EVENT FOREIGN KEY (EVENT_ID) REFERENCES VK_EVENT(ID)
);

CREATE TABLE VK_ASSET (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EVENT_ID NUMBER(19),
    PARTY_ID NUMBER(19),
    PLACE_ID NUMBER(19),
    ASSET_TYPE VARCHAR2(50) NOT NULL,
    FILE_NAME VARCHAR2(500),
    MIME_TYPE VARCHAR2(200),
    FILE_SIZE_BYTES NUMBER(19),
